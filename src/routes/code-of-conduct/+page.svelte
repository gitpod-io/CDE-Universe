<h1 class="h2 mb-small text-left">Code of Conduct</h1>
<div class="mx-auto mb-medium max-w-xl">
  <h2 class="text-h4 !mb-xx-small">Our Pledge</h2>
  <p>
    We as organisers, contributors, and leaders pledge to make participation in
    our community a harassment-free experience for everyone, regardless of age,
    body size, visible or invisible disability, ethnicity, sex characteristics,
    gender identity and expression, level of experience, education,
    socio-economic status, nationality, personal appearance, race, religion, or
    sexual identity and orientation.
    <br /> <br />
    We pledge to act and interact in ways that contribute to an open, welcoming,
    diverse, inclusive, and healthy community.
  </p>
  <h2 class="text-h4 !mb-xx-small mt-small">Our Standards</h2>
  <p>
    Examples of behavior that contributes to a positive environment for our
    community include:
    <br /> <br />
  </p>
  <ul class="ml-4 list-disc md:ml-8">
    <li>Demonstrating empathy and kindness toward other people</li>
    <li>Being respectful of differing opinions, viewpoints, and experiences</li>
    <li>Giving and gracefully accepting constructive feedback</li>
    <li>
      Accepting responsibility and apologizing to those affected by our
      mistakes, and learning from the experience
    </li>
    <li>
      Focusing on what is best not just for us as individuals, but for the
      overall community
    </li>
  </ul>
  <p class="mt-x-small">
    Examples of unacceptable behavior include:<br /><br />
  </p>
  <ul class="ml-4 list-disc md:ml-8 mb-small">
    <li>
      The use of sexualized language or imagery, and sexual attention or
      advances of any kind
    </li>
    <li>
      Trolling, insulting or derogatory comments, and personal or political
      attacks
    </li>
    <li>Public or private harassment</li>
    <li>
      Publishing others' private information, such as a physical or email
      address, without their explicit permission
    </li>
    <li>
      Other conduct which could reasonably be considered inappropriate in a
      professional setting
    </li>
  </ul>
  <h2 class="text-h4 !mb-xx-small">Enforcement Responsibilities</h2>
  <p class="!mb-x-small">
    Community leaders are responsible for clarifying and enforcing our standards
    of acceptable behavior and will take appropriate and fair corrective action
    in response to any behavior that they deem inappropriate, threatening,
    offensive, or harmful.
    <br /> <br />
    Community leaders have the right and responsibility to remove, edit, or reject
    comments, commits, code, wiki edits, issues, and other contributions that are
    not aligned to this Code of Conduct, and will communicate reasons for moderation
    decisions when appropriate.
  </p>
  <h2 class="text-h4 !mb-xx-small">Scope</h2>
  <p class="!mb-x-small">
    This Code of Conduct applies within all community spaces, and also applies
    when an individual is officially representing the community in public
    spaces. Examples of representing our community include using an official
    e-mail address, posting via an official social media account, or acting as
    an appointed representative at an online or offline event.
  </p>
  <h2 class="text-h4 !mb-xx-small">Enforcement</h2>
  <p class="!mb-x-small">
    Instances of abusive, harassing, or otherwise unacceptable behavior may be
    reported to the community leaders responsible for enforcement at
    contact@confxconf.org. All complaints will be reviewed and investigated
    promptly and fairly.
    <br /> <br />
    All community leaders are obligated to respect the privacy and security of the
    reporter of any incident.
  </p>
  <h2 class="text-h4 !mb-xx-small">Enforcement Guidelines</h2>
  <p class="!mb-x-small">
    Community leaders will follow these Community Impact Guidelines in
    determining the consequences for any action they deem in violation of this
    Code of Conduct:
  </p>
  <ul class="ml-4 list-decimal md:ml-8 mb-small">
    <li class="text-lg"><strong>Correction</strong></li>
    <p class="!mb-x-small">
      Community Impact: Use of inappropriate language or other behavior deemed
      unprofessional or unwelcome in the community.
      <br /> <br />
      Consequence: A private, written warning from community leaders, providing clarity
      around the nature of the violation and an explanation of why the behavior was
      inappropriate. A public apology may be requested.
    </p>
    <li class="text-lg"><strong>Warning</strong></li>
    <p class="!mb-x-small">
      Community Impact: A violation through a single incident or series of
      actions.
      <br /> <br />
      Consequence: A warning with consequences for continued behavior. No interaction
      with the people involved, including unsolicited interaction with those enforcing
      the Code of Conduct, for a specified period of time. This includes avoiding
      interactions in community spaces as well as external channels like social media.
      Violating these terms may lead to a temporary or permanent ban.
    </p>
    <li class="text-lg"><strong>Temporary ban</strong></li>
    <p class="!mb-x-small">
      Community Impact: A serious violation of community standards, including
      sustained inappropriate behavior.
      <br /> <br />
      Consequence: A temporary ban from any sort of interaction or public communication
      with the community for a specified period of time. No public or private interaction
      with the people involved, including unsolicited interaction with those enforcing
      the Code of Conduct, is allowed during this period. Violating these terms may
      lead to a permanent ban.
    </p>
    <li class="text-lg"><strong>Permanent ban</strong></li>
    <p class="!mb-x-small">
      Community Impact: Demonstrating a pattern of violation of community
      standards, including sustained inappropriate behavior, harassment of an
      individual, or aggression toward or disparagement of classes of
      individuals.
      <br /> <br />
      Consequence: A permanent ban from any sort of public interaction within the
      community.
    </p>
  </ul>
